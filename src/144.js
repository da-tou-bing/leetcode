/**
 * Definition for a binary tree node.
 * function TreeNode(val) {
 *     this.val = val;
 *     this.left = this.right = null;
 * }
 */

/**
 * @param {TreeNode} root
 * @return {number[]}
 */

var preorderTraversal = function (root) {
  const res = [];
  traversal = (root) => {
    root && res.push(root.val);
    root && root.left && traversal(root.left);
    root && root.right && traversal(root.right);
  }
  traversal(root);
  console.log(`res`,res);
  return res;
};

const root = {
  val: 1,
  left: null,
  right: {
    val: 2,
    left: {
      val: 3,
      left: null,
      right: null
    },
    right: null
  }
};

preorderTraversal(root);